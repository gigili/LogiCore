package dev.gacbl.logicore.network.payload;

import dev.gacbl.logicore.LogiCore;
import dev.gacbl.logicore.client.ClientCycleData;
import net.minecraft.network.RegistryFriendlyByteBuf;
import net.minecraft.network.codec.ByteBufCodecs;
import net.minecraft.network.codec.StreamCodec;
import net.minecraft.network.protocol.common.custom.CustomPacketPayload;
import net.minecraft.resources.ResourceLocation;
import net.neoforged.neoforge.network.handling.IPayloadHandler;
import org.jetbrains.annotations.NotNull;

public record SyncPlayerCyclesPayload(long cycles) implements CustomPacketPayload {
    public static final Type<SyncPlayerCyclesPayload> TYPE = new Type<>(ResourceLocation.fromNamespaceAndPath(LogiCore.MOD_ID, "sync_player_cycles"));

    public static final StreamCodec<RegistryFriendlyByteBuf, SyncPlayerCyclesPayload> STREAM_CODEC = StreamCodec.composite(
            ByteBufCodecs.VAR_LONG, SyncPlayerCyclesPayload::cycles,
            SyncPlayerCyclesPayload::new
    );

    @Override
    public @NotNull Type<? extends CustomPacketPayload> type() {
        return TYPE;
    }

    public static final IPayloadHandler<SyncPlayerCyclesPayload> HANDLER = (payload, context) -> {
        context.enqueueWork(() -> {
            ClientCycleData.setCycles(payload.cycles());
        });
    };
}
